  <h2>Buildings on Riparian Land Come Down in Nairobi Demolitions</h2>
  <div class="info">Posted on: Aug 20, 2018</div>
  <img src="https://www.buyrentkenya.com/news/wp-content/uploads/sites/21/2018/08/Unsafe-Buildings-in-Nairobi-Demolished_Featured-Image1200by630-696x365.png" class="thumbnail img-responsive"  alt="">
 <p>With the recent Nairobi demolitions taking place in the country, a number of people are using the word “riparian” and some have no idea what it means. Riparian land simply means land that is between a river or stream. Building on such land could cause potential danger in that it weakens the building’s structure making it unsafe for people to occupy it. Why then do people construct their buildings in unsafe places? </p>
<br>
<b>Who approves the buildings for construction?</b>
  <img src="https://www.buyrentkenya.com/news/wp-content/uploads/sites/21/2018/08/Who-approves-the-buildings-for-construction-_1200by630.png" class="thumbnail img-responsive"  alt="">
 <p>When you decide to construct, you need to follow the right procedures. This will ensure you don’t incur massive losses when demolitions are going on. With the assumption that you have already purchased the piece of land, you need to submit your application to the National Construction Authority. After all the necessary documentation is filled, the form is returned with the receipt of payment for the fees for approval.</p>
 <p>The application should have a copy of the architect’s license. This application is sent to different departments for approval: Public Health, Water Authority, Electricity Authority, Physical Planning, and Road Department.</p>
 <p>The departments will take at least a week to make approvals then will give the application to the technical committee to make its approvals.</p>
 <p> After this, the application goes back to the Nairobi City County with the plans for the structure for a second approval. Construction will only begin after this approval. NEMA will do the environmental inspections to make sure that the building is constructed within the stipulated requirements.</p>
<p>Following this process will help you know whether you are building on the right reserve as opposed to riparian land or an area that the road will pass through.</p>

